
def print_greeting(name):
    """
    Prints out a greeting message for the person with name 'name' using
    the standard output.
    """
    print('Hi ', name)

def say_goodbye(name, goodbye_text='goodbye'):
    """
    Prints out a goodbye message for the person with the name given
    using the standard output.
    """
    print(goodbye_text, ' ', name)

